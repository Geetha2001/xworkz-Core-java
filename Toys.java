class Toys{

static String companyName;
static String toyName;
static int price;
static String colour;
static String warranty;

static void setCompanyName(String com){
companyName=com;
}
static String getCompanyName(){
return companyName;
}

static void setToyName(String name){
toyName=name;
}
static String getToyName(){
return toyName;
}

static void setPrice(int amt){
price=amt;
}
static int getPrice(){
return price;
}

static void setColour(String clr){
colour=clr;
}
static String getColour(){
return colour;
}

static void setWarranty(String exp){
warranty=exp;
}
static String getWarranty(){
return warranty;
}
}
