class Vessel{

static String name;
static String color;
static int price;
static String type;

static void setName(String vName){
name=vName;
}
static String getName(){
return name;
}

static void setColor(String colr){
color=colr;
}
static String getColor(){
return color;
}

static void setPrice(int amt){
price=amt;
}
static int getPrice(){
return price;
}

static void setType(String vType){
type=vType;
}
static String getType(){
return type;
}
}
